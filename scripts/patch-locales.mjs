import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dir = path.join(__dirname, '..', 'src', 'ui', 'locales');

function patch(code, overrides) {
  const data = JSON.parse(fs.readFileSync(path.join(dir, `${code}.json`), 'utf8'));
  Object.assign(data, overrides);
  fs.writeFileSync(path.join(dir, `${code}.json`), JSON.stringify(data, null, 2) + '\n');
  console.log(`Patched ${code}.json`);
}

